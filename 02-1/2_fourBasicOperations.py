# 사칙 연산
a = 4
b = 2

print(a + b)
print(a - b)
print(a * b)
print(a / b)

print(a // b)
print(a % b)


# x ** y처럼 사용했을 때 x의 y제곱을 나타내는 ** 연산자
aa = 3
ab = 4
print(aa ** ab)
# 3*3*3*3


# 복합 연산자
# 복합 연산자는 앞서 살펴본 +, - 와 같은 산술 연산자와 대입 연산자(=)를 합쳐 놓은 것
aaa = 1
aaa += 1
print(aaa)
# aaa = aaa + 1

aab = 1
aab -= 1
print(aab)
# aab = aab - 1

# +=, -=, *=, /=, //=, %=, **=