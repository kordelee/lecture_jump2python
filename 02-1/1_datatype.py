# 정수형
# 정수형(integer)이란 말 그대로 정수를 뜻하는 자료형을 말한다.

a = 123
b = -178
c = 0

print(a)
print(b)
print(c)


# 실수형
# 파이썬에서 실수형(floating-point)은 소수점이 포함된 숫자를 말한다.

aa = 1.2
ab = -3.45

print(aa)
print(ab)

# 실수형(지수)
# e와 E 둘 중 어느 것을 사용해도 된다.

aaa = 4.24E10
aab = 4.24e10
aac = 4.24e-10

print(aaa)
print(aab)
print(aac)


